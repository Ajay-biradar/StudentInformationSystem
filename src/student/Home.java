    public boolean isEmptyStudent(){
        if(jTextField2.getText().isEmpty()){
            JOptionPane.showMessageDialog(this,"Student name is missing");
            return false;
        }
        if(jDateChooser2.getDate()==null){
            JOptionPane.showMessageDialog(this,"Student data of birth is missing");
            return false;
        }
        if(jDateChooser2.getDate().compareTo(new Date())>0){
            JOptionPane.showMessageDialog(this,"No student from the future are allowed");
            return false;
        }
        if(jTextField3.getText().isEmpty()){
            JOptionPane.showMessageDialog(this,"Student EmailID is missing");
            return false;
        }
        if(!jTextField3.getText().matches("^.+@.+\\..+$")){
            JOptionPane.showMessageDialog(this,"Invalid email address");
            return false;
        }
        if(jTextField4.getText().isEmpty()){
            JOptionPane.showMessageDialog(this,"Student Phone number is missing");
            return false;
        }
        if(jTextField4.getText().length()>=15){
            JOptionPane.showMessageDialog(this,"Phone number is too long");
            return false;
        }
        if(jTextField5.getText().isEmpty()){
            JOptionPane.showMessageDialog(this,"Student father's name is missing");
            return false;
        }
        if(jTextField6.getText().isEmpty()){
            JOptionPane.showMessageDialog(this,"Student mother's  name is missing");
            return false;
        }
        if(jTextField7.getText().isEmpty()){
            JOptionPane.showMessageDialog(this,"Address Lin1  is missing");
            return false;
        }
         if(jTextField8.getText().isEmpty()){
            JOptionPane.showMessageDialog(this,"Address Lin2 is missing");
            return false;
         }
          if(imagePath==null){
            JOptionPane.showMessageDialog(this,"Please add your image");
            return false;
          }
         return true;
            
    }
    
    
    
    
